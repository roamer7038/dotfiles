#!/bin/sh
# ============================================================
# xinit.sh
# ============================================================
#
# 概要:
#   X Window System起動時の初期化設定スクリプト
#
# 用途:
#   X起動時に各種設定を適用
#   - スクリーンセーバー設定
#   - 電源管理（DPMS）設定
#   - キーボードリピート設定
#   - キーマップ設定
#
# 依存関係:
#   - xset（X設定ツール）
#   - xmodmap（キーマップ変更ツール）
#
# 使用例:
#   $ ./xinit.sh
#
# 自動実行の設定:
#   # .xprofile または .xinitrc に追加
#   ~/dotfiles/bin/xinit.sh
#
#   # i3wm の config に追加
#   exec --no-startup-id ~/dotfiles/bin/xinit.sh
#
# 注意事項:
#   - X Window System環境でのみ動作します
#   - キーマップファイル（~/.Xmodmap）が必要です
#
# ============================================================

###########################################################
# スクリーンセーバーの設定
###########################################################

# xset s <timeout> <cycle>
# timeout: スクリーンセーバー起動までの秒数
# cycle: スクリーンセーバーのサイクル時間（秒）
#
# 設定値: 300秒（5分）でスクリーンセーバーを起動
xset s 300 300

###########################################################
# DPMS（Display Power Management Signaling）の設定
###########################################################

# xset dpms <standby> <suspend> <off>
# standby: スタンバイモードまでの秒数
# suspend: サスペンドモードまでの秒数
# off: ディスプレイをオフにするまでの秒数
#
# 設定値: 300秒（5分）で各段階に移行
# - 5分後にスタンバイ
# - 5分後にサスペンド
# - 5分後にディスプレイオフ
xset dpms 300 300 300

###########################################################
# キーボードリピートの設定
###########################################################

# xset r rate <delay> <rate>
# delay: キーを押し続けてからリピートが始まるまでの遅延時間（ミリ秒）
# rate: 1秒間のリピート回数
#
# 設定値:
# - delay: 300ミリ秒（0.3秒）
# - rate: 30回/秒
xset r rate 300 30

###########################################################
# キーマップの変更
###########################################################

# xmodmapで~/.Xmodmapに定義されたキーマップを適用
# 例: CapsLockをCtrlに変更、等のカスタマイズ
xmodmap ~/.Xmodmap
