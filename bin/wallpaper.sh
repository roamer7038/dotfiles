#!/bin/sh
# ============================================================
# wallpaper.sh
# ============================================================
#
# 概要:
#   ~/.wallpaper ディレクトリ内の画像をランダムに選択して壁紙に設定
#
# 用途:
#   X Window System環境でデスクトップ壁紙をランダムに変更
#   起動時やログイン時に実行することで、毎回異なる壁紙を表示可能
#
# 依存関係:
#   - feh（画像ビューア・壁紙設定ツール）
#
# 使用方法:
#   1. ~/.wallpaper ディレクトリに壁紙用の画像を配置
#   2. このスクリプトを実行
#
# 使用例:
#   $ ./wallpaper.sh
#
# 自動実行の設定例:
#   # .xprofile または .xinitrc に追加
#   ~/dotfiles/bin/wallpaper.sh
#
#   # i3wm の config に追加
#   exec --no-startup-id ~/dotfiles/bin/wallpaper.sh
#
# 対応画像形式:
#   fehがサポートする形式（JPEG, PNG, GIF, BMP等）
#
# 注意事項:
#   - ~/.wallpaper ディレクトリが存在しない場合は自動作成されます
#   - 画像ファイルがない場合、エラーは発生しません
#
# ============================================================

# 壁紙ディレクトリを作成（存在しない場合）
mkdir -p ~/.wallpaper

# fehコマンドで壁紙を設定
# --randomize: ランダムに画像を選択
# --bg-fill: 画面サイズに合わせて画像を拡大（アスペクト比維持）
feh --randomize --bg-fill ~/.wallpaper/*
